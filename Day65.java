
package day65;

public class Day65 {

    public static void main(String[] args) {
        int A = 5;
        int B = 7;
        boolean hasil;
       
        hasil = A > B;
        System.out.println(hasil);
       
        hasil = A < B;
        System.out.println(hasil);
        
        hasil = A >= B;
        System.out.println(hasil);
       
        hasil = A <= B;
        System.out.println(hasil);
        
        hasil = A == B;
        System.out.println(hasil);

        hasil = A != B;
        System.out.println(hasil);
    }
    
}
